func renameFlag(service, operation, flag string) string {
	for pattern, name := range argumentRenames {
		if pattern == fmt.Sprintf("%s.%s.%s", service, operation, flag) ||
			pattern == fmt.Sprintf("%s.*.%s", service, flag) {
			return name
		}
	}
	return flag
}
