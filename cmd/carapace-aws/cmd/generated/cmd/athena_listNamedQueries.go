package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listNamedQueriesCmd = &cobra.Command{
	Use:   "list-named-queries",
	Short: "Provides a list of available query IDs only for queries saved in the specified workgroup.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listNamedQueriesCmd).Standalone()

	athena_listNamedQueriesCmd.Flags().String("max-results", "", "The maximum number of queries to return in this request.")
	athena_listNamedQueriesCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
	athena_listNamedQueriesCmd.Flags().String("work-group", "", "The name of the workgroup from which the named queries are being returned.")
	athenaCmd.AddCommand(athena_listNamedQueriesCmd)
}
