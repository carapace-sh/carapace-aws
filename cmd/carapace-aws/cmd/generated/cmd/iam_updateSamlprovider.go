package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var iam_updateSamlproviderCmd = &cobra.Command{
	Use:   "update-samlprovider",
	Short: "Updates the metadata document, SAML encryption settings, and private keys for an existing SAML provider.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(iam_updateSamlproviderCmd).Standalone()

	iam_updateSamlproviderCmd.Flags().String("add-private-key", "", "Specifies the new private key from your external identity provider.")
	iam_updateSamlproviderCmd.Flags().String("assertion-encryption-mode", "", "Specifies the encryption setting for the SAML provider.")
	iam_updateSamlproviderCmd.Flags().String("remove-private-key", "", "The Key ID of the private key to remove.")
	iam_updateSamlproviderCmd.Flags().String("samlmetadata-document", "", "An XML document generated by an identity provider (IdP) that supports SAML 2.0.")
	iam_updateSamlproviderCmd.Flags().String("samlprovider-arn", "", "The Amazon Resource Name (ARN) of the SAML provider to update.")
	iam_updateSamlproviderCmd.MarkFlagRequired("samlprovider-arn")
	iamCmd.AddCommand(iam_updateSamlproviderCmd)
}
