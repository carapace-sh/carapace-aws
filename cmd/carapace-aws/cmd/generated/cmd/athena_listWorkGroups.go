package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listWorkGroupsCmd = &cobra.Command{
	Use:   "list-work-groups",
	Short: "Lists available workgroups for the account.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listWorkGroupsCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(athena_listWorkGroupsCmd).Standalone()

		athena_listWorkGroupsCmd.Flags().String("max-results", "", "The maximum number of workgroups to return in this request.")
		athena_listWorkGroupsCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
	})
	athenaCmd.AddCommand(athena_listWorkGroupsCmd)
}
