package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var amplify_startDeploymentCmd = &cobra.Command{
	Use:   "start-deployment",
	Short: "Starts a deployment for a manually deployed app.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(amplify_startDeploymentCmd).Standalone()

	amplify_startDeploymentCmd.Flags().String("app-id", "", "The unique ID for an Amplify app.")
	amplify_startDeploymentCmd.Flags().String("branch-name", "", "The name of the branch to use for the deployment job.")
	amplify_startDeploymentCmd.Flags().String("job-id", "", "The job ID for this deployment that is generated by the `CreateDeployment` request.")
	amplify_startDeploymentCmd.Flags().String("source-url", "", "The source URL for the deployment that is used when calling `StartDeployment` without `CreateDeployment`.")
	amplify_startDeploymentCmd.Flags().String("source-url-type", "", "The type of source specified by the `sourceURL`.")
	amplify_startDeploymentCmd.MarkFlagRequired("app-id")
	amplify_startDeploymentCmd.MarkFlagRequired("branch-name")
	amplifyCmd.AddCommand(amplify_startDeploymentCmd)
}
