package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var databrew_listRulesetsCmd = &cobra.Command{
	Use:   "list-rulesets",
	Short: "List all rulesets available in the current account or rulesets associated with a specific resource (dataset).",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(databrew_listRulesetsCmd).Standalone()

	databrew_listRulesetsCmd.Flags().String("max-results", "", "The maximum number of results to return in this request.")
	databrew_listRulesetsCmd.Flags().String("next-token", "", "A token generated by DataBrew that specifies where to continue pagination if a previous request was truncated.")
	databrew_listRulesetsCmd.Flags().String("target-arn", "", "The Amazon Resource Name (ARN) of a resource (dataset).")
	databrewCmd.AddCommand(databrew_listRulesetsCmd)
}
