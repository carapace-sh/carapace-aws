package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var iam_getServiceLastAccessedDetailsCmd = &cobra.Command{
	Use:   "get-service-last-accessed-details",
	Short: "Retrieves a service last accessed report that was created using the `GenerateServiceLastAccessedDetails` operation.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(iam_getServiceLastAccessedDetailsCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(iam_getServiceLastAccessedDetailsCmd).Standalone()

		iam_getServiceLastAccessedDetailsCmd.Flags().String("job-id", "", "The ID of the request generated by the [GenerateServiceLastAccessedDetails](https://docs.aws.amazon.com/IAM/latest/APIReference/API_GenerateServiceLastAccessedDetails.html) operation.")
		iam_getServiceLastAccessedDetailsCmd.Flags().String("marker", "", "Use this parameter only when paginating results and only after you receive a response indicating that the results are truncated.")
		iam_getServiceLastAccessedDetailsCmd.Flags().String("max-items", "", "Use this only when paginating results to indicate the maximum number of items you want in the response.")
		iam_getServiceLastAccessedDetailsCmd.MarkFlagRequired("job-id")
	})
	iamCmd.AddCommand(iam_getServiceLastAccessedDetailsCmd)
}
