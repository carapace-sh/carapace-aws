package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var omics_startAnnotationImportJobCmd = &cobra.Command{
	Use:   "start-annotation-import-job",
	Short: "Amazon Web Services HealthOmics variant stores and annotation stores will no longer be open to new customers starting November 7, 2025. If you would like to use variant stores or annotation stores, sign up prior to that date.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(omics_startAnnotationImportJobCmd).Standalone()

	omics_startAnnotationImportJobCmd.Flags().String("annotation-fields", "", "The annotation schema generated by the parsed annotation data.")
	omics_startAnnotationImportJobCmd.Flags().String("destination-name", "", "A destination annotation store for the job.")
	omics_startAnnotationImportJobCmd.Flags().String("format-options", "", "Formatting options for the annotation file.")
	omics_startAnnotationImportJobCmd.Flags().String("items", "", "Items to import.")
	omics_startAnnotationImportJobCmd.Flags().String("role-arn", "", "A service role for the job.")
	omics_startAnnotationImportJobCmd.Flags().String("run-left-normalization", "", "The job's left normalization setting.")
	omics_startAnnotationImportJobCmd.Flags().String("version-name", "", "The name of the annotation store version.")
	omics_startAnnotationImportJobCmd.MarkFlagRequired("destination-name")
	omics_startAnnotationImportJobCmd.MarkFlagRequired("items")
	omics_startAnnotationImportJobCmd.MarkFlagRequired("role-arn")
	omicsCmd.AddCommand(omics_startAnnotationImportJobCmd)
}
