package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var guardduty_createThreatIntelSetCmd = &cobra.Command{
	Use:   "create-threat-intel-set",
	Short: "Creates a new ThreatIntelSet.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(guardduty_createThreatIntelSetCmd).Standalone()

	guardduty_createThreatIntelSetCmd.Flags().Bool("activate", false, "A Boolean value that indicates whether GuardDuty is to start using the uploaded ThreatIntelSet.")
	guardduty_createThreatIntelSetCmd.Flags().String("client-token", "", "The idempotency token for the create request.")
	guardduty_createThreatIntelSetCmd.Flags().String("detector-id", "", "The unique ID of the detector of the GuardDuty account for which you want to create a `threatIntelSet`.")
	guardduty_createThreatIntelSetCmd.Flags().String("expected-bucket-owner", "", "The Amazon Web Services account ID that owns the Amazon S3 bucket specified in the **location** parameter.")
	guardduty_createThreatIntelSetCmd.Flags().String("format", "", "The format of the file that contains the ThreatIntelSet.")
	guardduty_createThreatIntelSetCmd.Flags().String("location", "", "The URI of the file that contains the ThreatIntelSet.")
	guardduty_createThreatIntelSetCmd.Flags().String("name", "", "A user-friendly ThreatIntelSet name displayed in all findings that are generated by activity that involves IP addresses included in this ThreatIntelSet.")
	guardduty_createThreatIntelSetCmd.Flags().Bool("no-activate", false, "A Boolean value that indicates whether GuardDuty is to start using the uploaded ThreatIntelSet.")
	guardduty_createThreatIntelSetCmd.Flags().String("tags", "", "The tags to be added to a new threat list resource.")
	guardduty_createThreatIntelSetCmd.MarkFlagRequired("activate")
	guardduty_createThreatIntelSetCmd.MarkFlagRequired("detector-id")
	guardduty_createThreatIntelSetCmd.MarkFlagRequired("format")
	guardduty_createThreatIntelSetCmd.MarkFlagRequired("location")
	guardduty_createThreatIntelSetCmd.MarkFlagRequired("name")
	guardduty_createThreatIntelSetCmd.Flag("no-activate").Hidden = true
	guardduty_createThreatIntelSetCmd.MarkFlagRequired("no-activate")
	guarddutyCmd.AddCommand(guardduty_createThreatIntelSetCmd)
}
