package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listNotebookSessionsCmd = &cobra.Command{
	Use:   "list-notebook-sessions",
	Short: "Lists, in descending order, the sessions that have been created in a notebook that are in an active state like `CREATING`, `CREATED`, `IDLE` or `BUSY`.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listNotebookSessionsCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(athena_listNotebookSessionsCmd).Standalone()

		athena_listNotebookSessionsCmd.Flags().String("max-results", "", "The maximum number of notebook sessions to return.")
		athena_listNotebookSessionsCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
		athena_listNotebookSessionsCmd.Flags().String("notebook-id", "", "The ID of the notebook to list sessions for.")
		athena_listNotebookSessionsCmd.MarkFlagRequired("notebook-id")
	})
	athenaCmd.AddCommand(athena_listNotebookSessionsCmd)
}
