package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listExecutorsCmd = &cobra.Command{
	Use:   "list-executors",
	Short: "Lists, in descending order, the executors that joined a session.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listExecutorsCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(athena_listExecutorsCmd).Standalone()

		athena_listExecutorsCmd.Flags().String("executor-state-filter", "", "A filter for a specific executor state.")
		athena_listExecutorsCmd.Flags().String("max-results", "", "The maximum number of executors to return.")
		athena_listExecutorsCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
		athena_listExecutorsCmd.Flags().String("session-id", "", "The session ID.")
		athena_listExecutorsCmd.MarkFlagRequired("session-id")
	})
	athenaCmd.AddCommand(athena_listExecutorsCmd)
}
