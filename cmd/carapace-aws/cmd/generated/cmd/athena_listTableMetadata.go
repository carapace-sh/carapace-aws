package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listTableMetadataCmd = &cobra.Command{
	Use:   "list-table-metadata",
	Short: "Lists the metadata for the tables in the specified data catalog database.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listTableMetadataCmd).Standalone()

	athena_listTableMetadataCmd.Flags().String("catalog-name", "", "The name of the data catalog for which table metadata should be returned.")
	athena_listTableMetadataCmd.Flags().String("database-name", "", "The name of the database for which table metadata should be returned.")
	athena_listTableMetadataCmd.Flags().String("expression", "", "A regex filter that pattern-matches table names.")
	athena_listTableMetadataCmd.Flags().String("max-results", "", "Specifies the maximum number of results to return.")
	athena_listTableMetadataCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
	athena_listTableMetadataCmd.Flags().String("work-group", "", "The name of the workgroup for which the metadata is being fetched.")
	athena_listTableMetadataCmd.MarkFlagRequired("catalog-name")
	athena_listTableMetadataCmd.MarkFlagRequired("database-name")
	athenaCmd.AddCommand(athena_listTableMetadataCmd)
}
