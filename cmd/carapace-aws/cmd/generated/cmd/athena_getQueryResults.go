package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_getQueryResultsCmd = &cobra.Command{
	Use:   "get-query-results",
	Short: "Streams the results of a single query execution specified by `QueryExecutionId` from the Athena query results location in Amazon S3.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_getQueryResultsCmd).Standalone()

	athena_getQueryResultsCmd.Flags().String("max-results", "", "The maximum number of results (rows) to return in this request.")
	athena_getQueryResultsCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
	athena_getQueryResultsCmd.Flags().String("query-execution-id", "", "The unique ID of the query execution.")
	athena_getQueryResultsCmd.Flags().String("query-result-type", "", "When you set this to `DATA_ROWS` or empty, `GetQueryResults` returns the query results in rows.")
	athena_getQueryResultsCmd.MarkFlagRequired("query-execution-id")
	athenaCmd.AddCommand(athena_getQueryResultsCmd)
}
