package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listNotebookMetadataCmd = &cobra.Command{
	Use:   "list-notebook-metadata",
	Short: "Displays the notebook files for the specified workgroup in paginated format.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listNotebookMetadataCmd).Standalone()

	athena_listNotebookMetadataCmd.Flags().String("filters", "", "Search filter string.")
	athena_listNotebookMetadataCmd.Flags().String("max-results", "", "Specifies the maximum number of results to return.")
	athena_listNotebookMetadataCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
	athena_listNotebookMetadataCmd.Flags().String("work-group", "", "The name of the Spark enabled workgroup to retrieve notebook metadata for.")
	athena_listNotebookMetadataCmd.MarkFlagRequired("work-group")
	athenaCmd.AddCommand(athena_listNotebookMetadataCmd)
}
