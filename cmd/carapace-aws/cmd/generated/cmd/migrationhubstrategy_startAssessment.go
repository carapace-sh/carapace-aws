package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var migrationhubstrategy_startAssessmentCmd = &cobra.Command{
	Use:   "start-assessment",
	Short: "Starts the assessment of an on-premises environment.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(migrationhubstrategy_startAssessmentCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(migrationhubstrategy_startAssessmentCmd).Standalone()

		migrationhubstrategy_startAssessmentCmd.Flags().String("assessment-data-source-type", "", "The data source type of an assessment to be started.")
		migrationhubstrategy_startAssessmentCmd.Flags().String("assessment-targets", "", "List of criteria for assessment.")
		migrationhubstrategy_startAssessmentCmd.Flags().String("s3bucket-for-analysis-data", "", "The S3 bucket used by the collectors to send analysis data to the service.")
		migrationhubstrategy_startAssessmentCmd.Flags().String("s3bucket-for-report-data", "", "The S3 bucket where all the reports generated by the service are stored.")
	})
	migrationhubstrategyCmd.AddCommand(migrationhubstrategy_startAssessmentCmd)
}
