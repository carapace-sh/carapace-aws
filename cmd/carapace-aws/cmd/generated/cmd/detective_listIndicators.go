package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var detective_listIndicatorsCmd = &cobra.Command{
	Use:   "list-indicators",
	Short: "Gets the indicators from an investigation.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(detective_listIndicatorsCmd).Standalone()

	detective_listIndicatorsCmd.Flags().String("graph-arn", "", "The Amazon Resource Name (ARN) of the behavior graph.")
	detective_listIndicatorsCmd.Flags().String("indicator-type", "", "For the list of indicators of compromise that are generated by Detective investigations, see [Detective investigations](https://docs.aws.amazon.com/detective/latest/userguide/detective-investigation-about.html).")
	detective_listIndicatorsCmd.Flags().String("investigation-id", "", "The investigation ID of the investigation report.")
	detective_listIndicatorsCmd.Flags().String("max-results", "", "Lists the maximum number of indicators in a page.")
	detective_listIndicatorsCmd.Flags().String("next-token", "", "Lists if there are more results available.")
	detective_listIndicatorsCmd.MarkFlagRequired("graph-arn")
	detective_listIndicatorsCmd.MarkFlagRequired("investigation-id")
	detectiveCmd.AddCommand(detective_listIndicatorsCmd)
}
