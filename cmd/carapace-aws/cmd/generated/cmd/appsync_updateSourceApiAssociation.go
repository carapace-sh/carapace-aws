package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var appsync_updateSourceApiAssociationCmd = &cobra.Command{
	Use:   "update-source-api-association",
	Short: "Updates some of the configuration choices of a particular source API association.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(appsync_updateSourceApiAssociationCmd).Standalone()

	appsync_updateSourceApiAssociationCmd.Flags().String("association-id", "", "The ID generated by the AppSync service for the source API association.")
	appsync_updateSourceApiAssociationCmd.Flags().String("description", "", "The description field.")
	appsync_updateSourceApiAssociationCmd.Flags().String("merged-api-identifier", "", "The identifier of the AppSync Merged API.")
	appsync_updateSourceApiAssociationCmd.Flags().String("source-api-association-config", "", "The `SourceApiAssociationConfig` object data.")
	appsync_updateSourceApiAssociationCmd.MarkFlagRequired("association-id")
	appsync_updateSourceApiAssociationCmd.MarkFlagRequired("merged-api-identifier")
	appsyncCmd.AddCommand(appsync_updateSourceApiAssociationCmd)
}
