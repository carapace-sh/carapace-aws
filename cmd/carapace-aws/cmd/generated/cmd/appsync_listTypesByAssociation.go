package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var appsync_listTypesByAssociationCmd = &cobra.Command{
	Use:   "list-types-by-association",
	Short: "Lists `Type` objects by the source API association ID.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(appsync_listTypesByAssociationCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(appsync_listTypesByAssociationCmd).Standalone()

		appsync_listTypesByAssociationCmd.Flags().String("association-id", "", "The ID generated by the AppSync service for the source API association.")
		appsync_listTypesByAssociationCmd.Flags().String("format", "", "The format type.")
		appsync_listTypesByAssociationCmd.Flags().String("max-results", "", "The maximum number of results that you want the request to return.")
		appsync_listTypesByAssociationCmd.Flags().String("merged-api-identifier", "", "The identifier of the AppSync Merged API.")
		appsync_listTypesByAssociationCmd.Flags().String("next-token", "", "An identifier that was returned from the previous call to this operation, which you can use to return the next set of items in the list.")
		appsync_listTypesByAssociationCmd.MarkFlagRequired("association-id")
		appsync_listTypesByAssociationCmd.MarkFlagRequired("format")
		appsync_listTypesByAssociationCmd.MarkFlagRequired("merged-api-identifier")
	})
	appsyncCmd.AddCommand(appsync_listTypesByAssociationCmd)
}
