package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var iam_createSamlproviderCmd = &cobra.Command{
	Use:   "create-samlprovider",
	Short: "Creates an IAM resource that describes an identity provider (IdP) that supports SAML 2.0.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(iam_createSamlproviderCmd).Standalone()

	iam_createSamlproviderCmd.Flags().String("add-private-key", "", "The private key generated from your external identity provider.")
	iam_createSamlproviderCmd.Flags().String("assertion-encryption-mode", "", "Specifies the encryption setting for the SAML provider.")
	iam_createSamlproviderCmd.Flags().String("name", "", "The name of the provider to create.")
	iam_createSamlproviderCmd.Flags().String("samlmetadata-document", "", "An XML document generated by an identity provider (IdP) that supports SAML 2.0.")
	iam_createSamlproviderCmd.Flags().String("tags", "", "A list of tags that you want to attach to the new IAM SAML provider.")
	iam_createSamlproviderCmd.MarkFlagRequired("name")
	iam_createSamlproviderCmd.MarkFlagRequired("samlmetadata-document")
	iamCmd.AddCommand(iam_createSamlproviderCmd)
}
