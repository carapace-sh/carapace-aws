package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listQueryExecutionsCmd = &cobra.Command{
	Use:   "list-query-executions",
	Short: "Provides a list of available query execution IDs for the queries in the specified workgroup.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listQueryExecutionsCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(athena_listQueryExecutionsCmd).Standalone()

		athena_listQueryExecutionsCmd.Flags().String("max-results", "", "The maximum number of query executions to return in this request.")
		athena_listQueryExecutionsCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
		athena_listQueryExecutionsCmd.Flags().String("work-group", "", "The name of the workgroup from which queries are being returned.")
	})
	athenaCmd.AddCommand(athena_listQueryExecutionsCmd)
}
