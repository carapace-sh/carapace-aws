package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listApplicationDpusizesCmd = &cobra.Command{
	Use:   "list-application-dpusizes",
	Short: "Returns the supported DPU sizes for the supported application runtimes (for example, `Athena notebook version 1`).",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listApplicationDpusizesCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(athena_listApplicationDpusizesCmd).Standalone()

		athena_listApplicationDpusizesCmd.Flags().String("max-results", "", "Specifies the maximum number of results to return.")
		athena_listApplicationDpusizesCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
	})
	athenaCmd.AddCommand(athena_listApplicationDpusizesCmd)
}
