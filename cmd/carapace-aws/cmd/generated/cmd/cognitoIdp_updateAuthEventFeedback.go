package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var cognitoIdp_updateAuthEventFeedbackCmd = &cobra.Command{
	Use:   "update-auth-event-feedback",
	Short: "Provides the feedback for an authentication event generated by threat protection features.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(cognitoIdp_updateAuthEventFeedbackCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(cognitoIdp_updateAuthEventFeedbackCmd).Standalone()

		cognitoIdp_updateAuthEventFeedbackCmd.Flags().String("event-id", "", "The ID of the authentication event that you want to submit feedback for.")
		cognitoIdp_updateAuthEventFeedbackCmd.Flags().String("feedback-token", "", "The feedback token, an encrypted object generated by Amazon Cognito and passed to your user in the notification email message from the event.")
		cognitoIdp_updateAuthEventFeedbackCmd.Flags().String("feedback-value", "", "Your feedback to the authentication event.")
		cognitoIdp_updateAuthEventFeedbackCmd.Flags().String("user-pool-id", "", "The ID of the user pool where you want to update auth event feedback.")
		cognitoIdp_updateAuthEventFeedbackCmd.Flags().String("username", "", "The name of the user that you want to query or modify.")
		cognitoIdp_updateAuthEventFeedbackCmd.MarkFlagRequired("event-id")
		cognitoIdp_updateAuthEventFeedbackCmd.MarkFlagRequired("feedback-token")
		cognitoIdp_updateAuthEventFeedbackCmd.MarkFlagRequired("feedback-value")
		cognitoIdp_updateAuthEventFeedbackCmd.MarkFlagRequired("user-pool-id")
		cognitoIdp_updateAuthEventFeedbackCmd.MarkFlagRequired("username")
	})
	cognitoIdpCmd.AddCommand(cognitoIdp_updateAuthEventFeedbackCmd)
}
