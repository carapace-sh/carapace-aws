package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var iot_listViolationEventsCmd = &cobra.Command{
	Use:   "list-violation-events",
	Short: "Lists the Device Defender security profile violations discovered during the given time period.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(iot_listViolationEventsCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(iot_listViolationEventsCmd).Standalone()

		iot_listViolationEventsCmd.Flags().String("behavior-criteria-type", "", "The criteria for a behavior.")
		iot_listViolationEventsCmd.Flags().String("end-time", "", "The end time for the alerts to be listed.")
		iot_listViolationEventsCmd.Flags().String("list-suppressed-alerts", "", "A list of all suppressed alerts.")
		iot_listViolationEventsCmd.Flags().String("max-results", "", "The maximum number of results to return at one time.")
		iot_listViolationEventsCmd.Flags().String("next-token", "", "The token for the next set of results.")
		iot_listViolationEventsCmd.Flags().String("security-profile-name", "", "A filter to limit results to those alerts generated by the specified security profile.")
		iot_listViolationEventsCmd.Flags().String("start-time", "", "The start time for the alerts to be listed.")
		iot_listViolationEventsCmd.Flags().String("thing-name", "", "A filter to limit results to those alerts caused by the specified thing.")
		iot_listViolationEventsCmd.Flags().String("verification-state", "", "The verification state of the violation (detect alarm).")
		iot_listViolationEventsCmd.MarkFlagRequired("end-time")
		iot_listViolationEventsCmd.MarkFlagRequired("start-time")
	})
	iotCmd.AddCommand(iot_listViolationEventsCmd)
}
