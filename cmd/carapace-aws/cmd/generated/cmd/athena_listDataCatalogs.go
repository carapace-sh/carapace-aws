package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listDataCatalogsCmd = &cobra.Command{
	Use:   "list-data-catalogs",
	Short: "Lists the data catalogs in the current Amazon Web Services account.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listDataCatalogsCmd).Standalone()

	athena_listDataCatalogsCmd.Flags().String("max-results", "", "Specifies the maximum number of data catalogs to return.")
	athena_listDataCatalogsCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
	athena_listDataCatalogsCmd.Flags().String("work-group", "", "The name of the workgroup.")
	athenaCmd.AddCommand(athena_listDataCatalogsCmd)
}
