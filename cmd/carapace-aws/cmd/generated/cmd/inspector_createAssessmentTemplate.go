package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var inspector_createAssessmentTemplateCmd = &cobra.Command{
	Use:   "create-assessment-template",
	Short: "Creates an assessment template for the assessment target that is specified by the ARN of the assessment target.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(inspector_createAssessmentTemplateCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(inspector_createAssessmentTemplateCmd).Standalone()

		inspector_createAssessmentTemplateCmd.Flags().String("assessment-target-arn", "", "The ARN that specifies the assessment target for which you want to create the assessment template.")
		inspector_createAssessmentTemplateCmd.Flags().String("assessment-template-name", "", "The user-defined name that identifies the assessment template that you want to create.")
		inspector_createAssessmentTemplateCmd.Flags().String("duration-in-seconds", "", "The duration of the assessment run in seconds.")
		inspector_createAssessmentTemplateCmd.Flags().String("rules-package-arns", "", "The ARNs that specify the rules packages that you want to attach to the assessment template.")
		inspector_createAssessmentTemplateCmd.Flags().String("user-attributes-for-findings", "", "The user-defined attributes that are assigned to every finding that is generated by the assessment run that uses this assessment template.")
		inspector_createAssessmentTemplateCmd.MarkFlagRequired("assessment-target-arn")
		inspector_createAssessmentTemplateCmd.MarkFlagRequired("assessment-template-name")
		inspector_createAssessmentTemplateCmd.MarkFlagRequired("duration-in-seconds")
		inspector_createAssessmentTemplateCmd.MarkFlagRequired("rules-package-arns")
	})
	inspectorCmd.AddCommand(inspector_createAssessmentTemplateCmd)
}
