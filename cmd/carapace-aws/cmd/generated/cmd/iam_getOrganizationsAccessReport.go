package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var iam_getOrganizationsAccessReportCmd = &cobra.Command{
	Use:   "get-organizations-access-report",
	Short: "Retrieves the service last accessed data report for Organizations that was previously generated using the `GenerateOrganizationsAccessReport` operation.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(iam_getOrganizationsAccessReportCmd).Standalone()

	iam_getOrganizationsAccessReportCmd.Flags().String("job-id", "", "The identifier of the request generated by the [GenerateOrganizationsAccessReport](https://docs.aws.amazon.com/IAM/latest/APIReference/API_GenerateOrganizationsAccessReport.html) operation.")
	iam_getOrganizationsAccessReportCmd.Flags().String("marker", "", "Use this parameter only when paginating results and only after you receive a response indicating that the results are truncated.")
	iam_getOrganizationsAccessReportCmd.Flags().String("max-items", "", "Use this only when paginating results to indicate the maximum number of items you want in the response.")
	iam_getOrganizationsAccessReportCmd.Flags().String("sort-key", "", "The key that is used to sort the results.")
	iam_getOrganizationsAccessReportCmd.MarkFlagRequired("job-id")
	iamCmd.AddCommand(iam_getOrganizationsAccessReportCmd)
}
