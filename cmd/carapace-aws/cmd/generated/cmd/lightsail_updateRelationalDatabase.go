package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var lightsail_updateRelationalDatabaseCmd = &cobra.Command{
	Use:   "update-relational-database",
	Short: "Allows the update of one or more attributes of a database in Amazon Lightsail.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(lightsail_updateRelationalDatabaseCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(lightsail_updateRelationalDatabaseCmd).Standalone()

		lightsail_updateRelationalDatabaseCmd.Flags().String("apply-immediately", "", "When `true`, applies changes immediately.")
		lightsail_updateRelationalDatabaseCmd.Flags().String("ca-certificate-identifier", "", "Indicates the certificate that needs to be associated with the database.")
		lightsail_updateRelationalDatabaseCmd.Flags().String("disable-backup-retention", "", "When `true`, disables automated backup retention for your database.")
		lightsail_updateRelationalDatabaseCmd.Flags().String("enable-backup-retention", "", "When `true`, enables automated backup retention for your database.")
		lightsail_updateRelationalDatabaseCmd.Flags().String("master-user-password", "", "The password for the master user.")
		lightsail_updateRelationalDatabaseCmd.Flags().String("preferred-backup-window", "", "The daily time range during which automated backups are created for your database if automated backups are enabled.")
		lightsail_updateRelationalDatabaseCmd.Flags().String("preferred-maintenance-window", "", "The weekly time range during which system maintenance can occur on your database.")
		lightsail_updateRelationalDatabaseCmd.Flags().String("publicly-accessible", "", "Specifies the accessibility options for your database.")
		lightsail_updateRelationalDatabaseCmd.Flags().String("relational-database-blueprint-id", "", "This parameter is used to update the major version of the database.")
		lightsail_updateRelationalDatabaseCmd.Flags().String("relational-database-name", "", "The name of your Lightsail database resource to update.")
		lightsail_updateRelationalDatabaseCmd.Flags().String("rotate-master-user-password", "", "When `true`, the master user password is changed to a new strong password generated by Lightsail.")
		lightsail_updateRelationalDatabaseCmd.MarkFlagRequired("relational-database-name")
	})
	lightsailCmd.AddCommand(lightsail_updateRelationalDatabaseCmd)
}
