package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var databrew_listJobsCmd = &cobra.Command{
	Use:   "list-jobs",
	Short: "Lists all of the DataBrew jobs that are defined.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(databrew_listJobsCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(databrew_listJobsCmd).Standalone()

		databrew_listJobsCmd.Flags().String("dataset-name", "", "The name of a dataset.")
		databrew_listJobsCmd.Flags().String("max-results", "", "The maximum number of results to return in this request.")
		databrew_listJobsCmd.Flags().String("next-token", "", "A token generated by DataBrew that specifies where to continue pagination if a previous request was truncated.")
		databrew_listJobsCmd.Flags().String("project-name", "", "The name of a project.")
	})
	databrewCmd.AddCommand(databrew_listJobsCmd)
}
