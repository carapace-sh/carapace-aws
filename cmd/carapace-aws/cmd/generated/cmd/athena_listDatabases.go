package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listDatabasesCmd = &cobra.Command{
	Use:   "list-databases",
	Short: "Lists the databases in the specified data catalog.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listDatabasesCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(athena_listDatabasesCmd).Standalone()

		athena_listDatabasesCmd.Flags().String("catalog-name", "", "The name of the data catalog that contains the databases to return.")
		athena_listDatabasesCmd.Flags().String("max-results", "", "Specifies the maximum number of results to return.")
		athena_listDatabasesCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
		athena_listDatabasesCmd.Flags().String("work-group", "", "The name of the workgroup for which the metadata is being fetched.")
		athena_listDatabasesCmd.MarkFlagRequired("catalog-name")
	})
	athenaCmd.AddCommand(athena_listDatabasesCmd)
}
