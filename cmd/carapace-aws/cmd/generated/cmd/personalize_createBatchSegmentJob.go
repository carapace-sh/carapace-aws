package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var personalize_createBatchSegmentJobCmd = &cobra.Command{
	Use:   "create-batch-segment-job",
	Short: "Creates a batch segment job.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(personalize_createBatchSegmentJobCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(personalize_createBatchSegmentJobCmd).Standalone()

		personalize_createBatchSegmentJobCmd.Flags().String("filter-arn", "", "The ARN of the filter to apply to the batch segment job.")
		personalize_createBatchSegmentJobCmd.Flags().String("job-input", "", "The Amazon S3 path for the input data used to generate the batch segment job.")
		personalize_createBatchSegmentJobCmd.Flags().String("job-name", "", "The name of the batch segment job to create.")
		personalize_createBatchSegmentJobCmd.Flags().String("job-output", "", "The Amazon S3 path for the bucket where the job's output will be stored.")
		personalize_createBatchSegmentJobCmd.Flags().String("num-results", "", "The number of predicted users generated by the batch segment job for each line of input data.")
		personalize_createBatchSegmentJobCmd.Flags().String("role-arn", "", "The ARN of the Amazon Identity and Access Management role that has permissions to read and write to your input and output Amazon S3 buckets respectively.")
		personalize_createBatchSegmentJobCmd.Flags().String("solution-version-arn", "", "The Amazon Resource Name (ARN) of the solution version you want the batch segment job to use to generate batch segments.")
		personalize_createBatchSegmentJobCmd.Flags().String("tags", "", "A list of [tags](https://docs.aws.amazon.com/personalize/latest/dg/tagging-resources.html) to apply to the batch segment job.")
		personalize_createBatchSegmentJobCmd.MarkFlagRequired("job-input")
		personalize_createBatchSegmentJobCmd.MarkFlagRequired("job-name")
		personalize_createBatchSegmentJobCmd.MarkFlagRequired("job-output")
		personalize_createBatchSegmentJobCmd.MarkFlagRequired("role-arn")
		personalize_createBatchSegmentJobCmd.MarkFlagRequired("solution-version-arn")
	})
	personalizeCmd.AddCommand(personalize_createBatchSegmentJobCmd)
}
