package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var mturk_createHitwithHittypeCmd = &cobra.Command{
	Use:   "create-hitwith-hittype",
	Short: "The `CreateHITWithHITType` operation creates a new Human Intelligence Task (HIT) using an existing HITTypeID generated by the `CreateHITType` operation.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(mturk_createHitwithHittypeCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(mturk_createHitwithHittypeCmd).Standalone()

		mturk_createHitwithHittypeCmd.Flags().String("assignment-review-policy", "", "The Assignment-level Review Policy applies to the assignments under the HIT.")
		mturk_createHitwithHittypeCmd.Flags().String("hitlayout-id", "", "The HITLayoutId allows you to use a pre-existing HIT design with placeholder values and create an additional HIT by providing those values as HITLayoutParameters.")
		mturk_createHitwithHittypeCmd.Flags().String("hitlayout-parameters", "", "If the HITLayoutId is provided, any placeholder values must be filled in with values using the HITLayoutParameter structure.")
		mturk_createHitwithHittypeCmd.Flags().String("hitreview-policy", "", "The HIT-level Review Policy applies to the HIT.")
		mturk_createHitwithHittypeCmd.Flags().String("hittype-id", "", "The HIT type ID you want to create this HIT with.")
		mturk_createHitwithHittypeCmd.Flags().String("lifetime-in-seconds", "", "An amount of time, in seconds, after which the HIT is no longer available for users to accept.")
		mturk_createHitwithHittypeCmd.Flags().String("max-assignments", "", "The number of times the HIT can be accepted and completed before the HIT becomes unavailable.")
		mturk_createHitwithHittypeCmd.Flags().String("question", "", "The data the person completing the HIT uses to produce the results.")
		mturk_createHitwithHittypeCmd.Flags().String("requester-annotation", "", "An arbitrary data field.")
		mturk_createHitwithHittypeCmd.Flags().String("unique-request-token", "", "A unique identifier for this request which allows you to retry the call on error without creating duplicate HITs.")
		mturk_createHitwithHittypeCmd.MarkFlagRequired("hittype-id")
		mturk_createHitwithHittypeCmd.MarkFlagRequired("lifetime-in-seconds")
	})
	mturkCmd.AddCommand(mturk_createHitwithHittypeCmd)
}
