package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var connect_updateViewContentCmd = &cobra.Command{
	Use:   "update-view-content",
	Short: "Updates the view content of the given view identifier in the specified Amazon Connect instance.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(connect_updateViewContentCmd).Standalone()

	connect_updateViewContentCmd.Flags().String("content", "", "View content containing all content necessary to render a view except for runtime input data and the runtime input schema, which is auto-generated by this operation.")
	connect_updateViewContentCmd.Flags().String("instance-id", "", "The identifier of the Amazon Connect instance.")
	connect_updateViewContentCmd.Flags().String("status", "", "Indicates the view status as either `SAVED` or `PUBLISHED`.")
	connect_updateViewContentCmd.Flags().String("view-id", "", "The identifier of the view.")
	connect_updateViewContentCmd.MarkFlagRequired("content")
	connect_updateViewContentCmd.MarkFlagRequired("instance-id")
	connect_updateViewContentCmd.MarkFlagRequired("status")
	connect_updateViewContentCmd.MarkFlagRequired("view-id")
	connectCmd.AddCommand(connect_updateViewContentCmd)
}
