package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var codepipeline_putThirdPartyJobSuccessResultCmd = &cobra.Command{
	Use:   "put-third-party-job-success-result",
	Short: "Represents the success of a third party job as returned to the pipeline by a job worker.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(codepipeline_putThirdPartyJobSuccessResultCmd).Standalone()

	codepipeline_putThirdPartyJobSuccessResultCmd.Flags().String("client-token", "", "The clientToken portion of the clientId and clientToken pair used to verify that the calling entity is allowed access to the job and its details.")
	codepipeline_putThirdPartyJobSuccessResultCmd.Flags().String("continuation-token", "", "A token generated by a job worker, such as a CodeDeploy deployment ID, that a successful job provides to identify a partner action in progress.")
	codepipeline_putThirdPartyJobSuccessResultCmd.Flags().String("current-revision", "", "Represents information about a current revision.")
	codepipeline_putThirdPartyJobSuccessResultCmd.Flags().String("execution-details", "", "The details of the actions taken and results produced on an artifact as it passes through stages in the pipeline.")
	codepipeline_putThirdPartyJobSuccessResultCmd.Flags().String("job-id", "", "The ID of the job that successfully completed.")
	codepipeline_putThirdPartyJobSuccessResultCmd.MarkFlagRequired("client-token")
	codepipeline_putThirdPartyJobSuccessResultCmd.MarkFlagRequired("job-id")
	codepipelineCmd.AddCommand(codepipeline_putThirdPartyJobSuccessResultCmd)
}
