package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listEngineVersionsCmd = &cobra.Command{
	Use:   "list-engine-versions",
	Short: "Returns a list of engine versions that are available to choose from, including the Auto option.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listEngineVersionsCmd).Standalone()

	athena_listEngineVersionsCmd.Flags().String("max-results", "", "The maximum number of engine versions to return in this request.")
	athena_listEngineVersionsCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
	athenaCmd.AddCommand(athena_listEngineVersionsCmd)
}
