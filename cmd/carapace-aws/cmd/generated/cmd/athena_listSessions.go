package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listSessionsCmd = &cobra.Command{
	Use:   "list-sessions",
	Short: "Lists the sessions in a workgroup that are in an active state like `CREATING`, `CREATED`, `IDLE`, or `BUSY`.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listSessionsCmd).Standalone()

	athena_listSessionsCmd.Flags().String("max-results", "", "The maximum number of sessions to return.")
	athena_listSessionsCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
	athena_listSessionsCmd.Flags().String("state-filter", "", "A filter for a specific session state.")
	athena_listSessionsCmd.Flags().String("work-group", "", "The workgroup to which the session belongs.")
	athena_listSessionsCmd.MarkFlagRequired("work-group")
	athenaCmd.AddCommand(athena_listSessionsCmd)
}
