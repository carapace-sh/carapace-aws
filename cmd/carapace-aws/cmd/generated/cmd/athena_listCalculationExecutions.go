package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listCalculationExecutionsCmd = &cobra.Command{
	Use:   "list-calculation-executions",
	Short: "Lists the calculations that have been submitted to a session in descending order.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listCalculationExecutionsCmd).Standalone()

	athena_listCalculationExecutionsCmd.Flags().String("max-results", "", "The maximum number of calculation executions to return.")
	athena_listCalculationExecutionsCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
	athena_listCalculationExecutionsCmd.Flags().String("session-id", "", "The session ID.")
	athena_listCalculationExecutionsCmd.Flags().String("state-filter", "", "A filter for a specific calculation execution state.")
	athena_listCalculationExecutionsCmd.MarkFlagRequired("session-id")
	athenaCmd.AddCommand(athena_listCalculationExecutionsCmd)
}
