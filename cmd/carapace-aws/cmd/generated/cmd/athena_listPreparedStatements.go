package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listPreparedStatementsCmd = &cobra.Command{
	Use:   "list-prepared-statements",
	Short: "Lists the prepared statements in the specified workgroup.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listPreparedStatementsCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(athena_listPreparedStatementsCmd).Standalone()

		athena_listPreparedStatementsCmd.Flags().String("max-results", "", "The maximum number of results to return in this request.")
		athena_listPreparedStatementsCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
		athena_listPreparedStatementsCmd.Flags().String("work-group", "", "The workgroup to list the prepared statements for.")
		athena_listPreparedStatementsCmd.MarkFlagRequired("work-group")
	})
	athenaCmd.AddCommand(athena_listPreparedStatementsCmd)
}
