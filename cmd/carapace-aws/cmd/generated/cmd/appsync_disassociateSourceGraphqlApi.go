package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var appsync_disassociateSourceGraphqlApiCmd = &cobra.Command{
	Use:   "disassociate-source-graphql-api",
	Short: "Deletes an association between a Merged API and source API using the Merged API's identifier and the association ID.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(appsync_disassociateSourceGraphqlApiCmd).Standalone()

	appsync_disassociateSourceGraphqlApiCmd.Flags().String("association-id", "", "The ID generated by the AppSync service for the source API association.")
	appsync_disassociateSourceGraphqlApiCmd.Flags().String("merged-api-identifier", "", "The identifier of the AppSync Merged API.")
	appsync_disassociateSourceGraphqlApiCmd.MarkFlagRequired("association-id")
	appsync_disassociateSourceGraphqlApiCmd.MarkFlagRequired("merged-api-identifier")
	appsyncCmd.AddCommand(appsync_disassociateSourceGraphqlApiCmd)
}
