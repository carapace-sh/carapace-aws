package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var customerProfiles_deleteProfileObjectCmd = &cobra.Command{
	Use:   "delete-profile-object",
	Short: "Removes an object associated with a profile of a given ProfileObjectType.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(customerProfiles_deleteProfileObjectCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(customerProfiles_deleteProfileObjectCmd).Standalone()

		customerProfiles_deleteProfileObjectCmd.Flags().String("domain-name", "", "The unique name of the domain.")
		customerProfiles_deleteProfileObjectCmd.Flags().String("object-type-name", "", "The name of the profile object type.")
		customerProfiles_deleteProfileObjectCmd.Flags().String("profile-id", "", "The unique identifier of a customer profile.")
		customerProfiles_deleteProfileObjectCmd.Flags().String("profile-object-unique-key", "", "The unique identifier of the profile object generated by the service.")
		customerProfiles_deleteProfileObjectCmd.MarkFlagRequired("domain-name")
		customerProfiles_deleteProfileObjectCmd.MarkFlagRequired("object-type-name")
		customerProfiles_deleteProfileObjectCmd.MarkFlagRequired("profile-id")
		customerProfiles_deleteProfileObjectCmd.MarkFlagRequired("profile-object-unique-key")
	})
	customerProfilesCmd.AddCommand(customerProfiles_deleteProfileObjectCmd)
}
