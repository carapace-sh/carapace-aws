package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var appsync_startSchemaMergeCmd = &cobra.Command{
	Use:   "start-schema-merge",
	Short: "Initiates a merge operation.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(appsync_startSchemaMergeCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(appsync_startSchemaMergeCmd).Standalone()

		appsync_startSchemaMergeCmd.Flags().String("association-id", "", "The ID generated by the AppSync service for the source API association.")
		appsync_startSchemaMergeCmd.Flags().String("merged-api-identifier", "", "The identifier of the AppSync Merged API.")
		appsync_startSchemaMergeCmd.MarkFlagRequired("association-id")
		appsync_startSchemaMergeCmd.MarkFlagRequired("merged-api-identifier")
	})
	appsyncCmd.AddCommand(appsync_startSchemaMergeCmd)
}
