package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var appsync_getSourceApiAssociationCmd = &cobra.Command{
	Use:   "get-source-api-association",
	Short: "Retrieves a `SourceApiAssociation` object.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(appsync_getSourceApiAssociationCmd).Standalone()

	appsync_getSourceApiAssociationCmd.Flags().String("association-id", "", "The ID generated by the AppSync service for the source API association.")
	appsync_getSourceApiAssociationCmd.Flags().String("merged-api-identifier", "", "The identifier of the AppSync Merged API.")
	appsync_getSourceApiAssociationCmd.MarkFlagRequired("association-id")
	appsync_getSourceApiAssociationCmd.MarkFlagRequired("merged-api-identifier")
	appsyncCmd.AddCommand(appsync_getSourceApiAssociationCmd)
}
