package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var athena_listCapacityReservationsCmd = &cobra.Command{
	Use:   "list-capacity-reservations",
	Short: "Lists the capacity reservations for the current account.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(athena_listCapacityReservationsCmd).Standalone()

	athena_listCapacityReservationsCmd.Flags().String("max-results", "", "Specifies the maximum number of results to return.")
	athena_listCapacityReservationsCmd.Flags().String("next-token", "", "A token generated by the Athena service that specifies where to continue pagination if a previous request was truncated.")
	athenaCmd.AddCommand(athena_listCapacityReservationsCmd)
}
