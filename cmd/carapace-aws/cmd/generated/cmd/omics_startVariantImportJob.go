package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var omics_startVariantImportJobCmd = &cobra.Command{
	Use:   "start-variant-import-job",
	Short: "Amazon Web Services HealthOmics variant stores and annotation stores will no longer be open to new customers starting November 7, 2025. If you would like to use variant stores or annotation stores, sign up prior to that date.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(omics_startVariantImportJobCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(omics_startVariantImportJobCmd).Standalone()

		omics_startVariantImportJobCmd.Flags().String("annotation-fields", "", "The annotation schema generated by the parsed annotation data.")
		omics_startVariantImportJobCmd.Flags().String("destination-name", "", "The destination variant store for the job.")
		omics_startVariantImportJobCmd.Flags().String("items", "", "Items to import.")
		omics_startVariantImportJobCmd.Flags().String("role-arn", "", "A service role for the job.")
		omics_startVariantImportJobCmd.Flags().String("run-left-normalization", "", "The job's left normalization setting.")
		omics_startVariantImportJobCmd.MarkFlagRequired("destination-name")
		omics_startVariantImportJobCmd.MarkFlagRequired("items")
		omics_startVariantImportJobCmd.MarkFlagRequired("role-arn")
	})
	omicsCmd.AddCommand(omics_startVariantImportJobCmd)
}
