package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var finspace_listEnvironmentsCmd = &cobra.Command{
	Use:   "list-environments",
	Short: "A list of all of your FinSpace environments.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(finspace_listEnvironmentsCmd).Standalone()

	finspace_listEnvironmentsCmd.Flags().String("max-results", "", "The maximum number of results to return in this request.")
	finspace_listEnvironmentsCmd.Flags().String("next-token", "", "A token generated by FinSpace that specifies where to continue pagination if a previous request was truncated.")
	finspaceCmd.AddCommand(finspace_listEnvironmentsCmd)
}
