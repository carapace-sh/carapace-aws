package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var odb_acceptMarketplaceRegistrationCmd = &cobra.Command{
	Use:   "accept-marketplace-registration",
	Short: "Registers the Amazon Web Services Marketplace token for your Amazon Web Services account to activate your Oracle Database@Amazon Web Services subscription.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(odb_acceptMarketplaceRegistrationCmd).Standalone()

	odb_acceptMarketplaceRegistrationCmd.Flags().String("marketplace-registration-token", "", "The registration token that's generated by Amazon Web Services Marketplace and sent to Oracle Database@Amazon Web Services.")
	odb_acceptMarketplaceRegistrationCmd.MarkFlagRequired("marketplace-registration-token")
	odbCmd.AddCommand(odb_acceptMarketplaceRegistrationCmd)
}
