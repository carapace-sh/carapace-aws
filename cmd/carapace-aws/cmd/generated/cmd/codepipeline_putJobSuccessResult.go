package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var codepipeline_putJobSuccessResultCmd = &cobra.Command{
	Use:   "put-job-success-result",
	Short: "Represents the success of a job as returned to the pipeline by a job worker.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(codepipeline_putJobSuccessResultCmd).PreRun(func(cmd *cobra.Command, args []string) {
		carapace.Gen(codepipeline_putJobSuccessResultCmd).Standalone()

		codepipeline_putJobSuccessResultCmd.Flags().String("continuation-token", "", "A token generated by a job worker, such as a CodeDeploy deployment ID, that a successful job provides to identify a custom action in progress.")
		codepipeline_putJobSuccessResultCmd.Flags().String("current-revision", "", "The ID of the current revision of the artifact successfully worked on by the job.")
		codepipeline_putJobSuccessResultCmd.Flags().String("execution-details", "", "The execution details of the successful job, such as the actions taken by the job worker.")
		codepipeline_putJobSuccessResultCmd.Flags().String("job-id", "", "The unique system-generated ID of the job that succeeded.")
		codepipeline_putJobSuccessResultCmd.Flags().String("output-variables", "", "Key-value pairs produced as output by a job worker that can be made available to a downstream action configuration.")
		codepipeline_putJobSuccessResultCmd.MarkFlagRequired("job-id")
	})
	codepipelineCmd.AddCommand(codepipeline_putJobSuccessResultCmd)
}
