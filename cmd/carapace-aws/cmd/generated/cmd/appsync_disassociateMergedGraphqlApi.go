package cmd

import (
	"github.com/carapace-sh/carapace"
	"github.com/spf13/cobra"
)

var appsync_disassociateMergedGraphqlApiCmd = &cobra.Command{
	Use:   "disassociate-merged-graphql-api",
	Short: "Deletes an association between a Merged API and source API using the source API's identifier and the association ID.",
	Run:   func(cmd *cobra.Command, args []string) {},
}

func init() {
	carapace.Gen(appsync_disassociateMergedGraphqlApiCmd).Standalone()

	appsync_disassociateMergedGraphqlApiCmd.Flags().String("association-id", "", "The ID generated by the AppSync service for the source API association.")
	appsync_disassociateMergedGraphqlApiCmd.Flags().String("source-api-identifier", "", "The identifier of the AppSync Source API.")
	appsync_disassociateMergedGraphqlApiCmd.MarkFlagRequired("association-id")
	appsync_disassociateMergedGraphqlApiCmd.MarkFlagRequired("source-api-identifier")
	appsyncCmd.AddCommand(appsync_disassociateMergedGraphqlApiCmd)
}
